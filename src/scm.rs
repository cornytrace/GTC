#[derive(Resource)]
struct ScriptEngine {
    scm: Cursor<Vec<u8>>,
    global_offset: u64,
    scripts: Vec<Script>,
    current_script: usize,
    obj_names: Vec<String>,
}

struct Script {
    name: String,
    local_vars: [u8; 16 * 4],
}

impl Default for Script {
    fn default() -> Self {
        Self {
            name: "noname".to_string(),
            local_vars: [0; 16 * 4],
        }
    }
}

#[derive(Clone, Debug)]
enum DataType {
    Eoal,
    Imm(i32),
    GlobalVar(u16),
    LocalVar(u16),
    ImmF(f32),
    String(String),
}

macro_rules! scm_matches {
    ($i:expr, $var_type:ident) => {
        match $i {
            DataType::$var_type(o) => o,
            _ => unimplemented!(),
        }
    };
}

impl ScriptEngine {
    fn new(scm: Vec<u8>) -> Self {
        let mut scm = Cursor::new(scm);
        scm.seek(std::io::SeekFrom::Start(3)).unwrap();
        let object_offset = scm.read_le::<u32>().unwrap() + 3 + 4 + 1;
        scm.seek(std::io::SeekFrom::Start(object_offset as u64))
            .unwrap();
        let num_objects = scm.read_le::<u32>().unwrap();
        let mut obj_names = Vec::with_capacity(num_objects as usize);
        for _ in 0..num_objects {
            obj_names.push(
                CStr::from_bytes_until_nul(&scm.read_le::<[u8; 24]>().unwrap())
                    .unwrap()
                    .to_str()
                    .unwrap()
                    .to_owned(),
            );
        }

        Self {
            scm,
            scripts: vec![Script::default()],
            current_script: 0,
            global_offset: 8,
            obj_names,
        }
    }

    fn step(&mut self, mut commands: Commands, mut handles: ResMut<ObjHandles>) {
        let scm = &mut self.scm;
        let opcode: u16 = scm.read_le().unwrap();
        let _inverted = opcode & 0x8000 != 0; // TODO
        let opcode = opcode & 0x7fff;
        let args = self.read_args(opcode_num_args(opcode));

        match opcode {
            0x0002 => {
                match args[0] {
                    DataType::Imm(i) => self.scm.seek(std::io::SeekFrom::Start(i as u64)).unwrap(),
                    _ => unimplemented!(),
                };
            }
            0x0004 => {
                let offset = scm_matches!(&args[0], GlobalVar);
                match &args[1] {
                    DataType::Imm(v) => {
                        self.set_global(*offset, DataType::Imm(*v));
                    }
                    _ => unimplemented!(),
                };
            }
            0x0005 => {
                let offset = scm_matches!(&args[0], GlobalVar);
                let value = scm_matches!(&args[1], ImmF);
                self.set_global(*offset, DataType::ImmF(*value));
            }
            0x0015 => {
                let offset = scm_matches!(&args[0], GlobalVar);
                let imm_value = scm_matches!(&args[1], ImmF);
                let mut glob_value = DataType::ImmF(0.);
                self.get_global(*offset, &mut glob_value);
                glob_value = match glob_value {
                    DataType::ImmF(f) => DataType::ImmF(f / imm_value),
                    _ => unimplemented!(),
                };
                self.set_global(*offset, glob_value);
            }
            0x0086 => {
                let offset_1 = scm_matches!(&args[0], GlobalVar);
                let offset_2 = scm_matches!(&args[1], GlobalVar);
                let mut val = DataType::ImmF(0.);
                self.get_global(*offset_2, &mut val);
                self.set_global(*offset_1, val);
            }
            0x004F => {
                debug!("0x004F arg length: {}", args.len());
            }
            0x029B => {
                let id: i32;
                let name: String;
                match &args[0] {
                    DataType::Imm(v) => {
                        id = *v;
                        name = String::from("");
                    }
                    DataType::String(s) => {
                        id = 0;
                        name = s.clone();
                    }
                    _ => unimplemented!(),
                }
                let x = *scm_matches!(&args[1], ImmF);
                let y = *scm_matches!(&args[2], ImmF);
                let z = *scm_matches!(&args[3], ImmF);
                let handle_loc = &args[4];

                let ent = commands.spawn_empty().id();
                let handle = &handles.insert(ent);

                match handle_loc {
                    DataType::GlobalVar(o) => {
                        self.set_global(*o, DataType::Imm(*handle as i32));
                    }
                    DataType::LocalVar(_i) => {
                        todo!()
                    }
                    _ => unimplemented!(),
                }

                let (id, name) = self.handle_neg_obj_id(id, name);
                commands.trigger(SpawnObject {
                    id,
                    name,
                    pos: [-x, z, y],
                    scale: [1., 1., 1.],
                    rot: Quat::IDENTITY,
                    handle: Some(ent),
                });
            }
            0x03A4 => match &args[0] {
                DataType::String(s) => self.scripts[self.current_script].name = s.clone(),
                _ => unimplemented!(),
            },
            c @ (0x042C | 0x030D) => {
                error!("opcode unimplemented: {:#x}", c);
            }
            c => {
                error!("unknown opcode: {:#x}", c);
            }
        };
    }

    fn get_global(&mut self, offset: u16, mut value: &mut DataType) {
        let old_pos = self.scm.position();
        self.scm.set_position(self.global_offset + (offset as u64));
        *value = match &mut value {
            DataType::Imm(_) => DataType::Imm(self.scm.read_le().unwrap()),
            DataType::ImmF(_) => DataType::ImmF(self.scm.read_le().unwrap()), // Read/Write to global as floating point, not fixed point
            DataType::String(_) => unimplemented!(),
            _ => unimplemented!(),
        };
        self.scm.set_position(old_pos);
    }

    fn set_global(&mut self, offset: u16, value: DataType) {
        let old_pos = self.scm.position();
        self.scm.set_position(self.global_offset + (offset as u64));
        match value {
            DataType::Imm(v) => self.scm.write_le(&v).unwrap(),
            DataType::ImmF(v) => self.scm.write_le(&v).unwrap(), // Read/Write to global as floating point, not fixed point
            DataType::String(_v) => unimplemented!(),
            _ => unimplemented!(),
        }
        self.scm.set_position(old_pos);
    }

    fn read_args(&mut self, num_args: u8) -> Vec<DataType> {
        let mut res = Vec::with_capacity(num_args as usize);
        for _ in 0..num_args {
            let ty = {
                match self.scm.read_le::<u8>().unwrap() {
                    0x00 => DataType::Eoal,
                    0x01 => DataType::Imm(self.scm.read_le().unwrap()),
                    0x02 => DataType::GlobalVar(self.scm.read_le().unwrap()),
                    0x03 => DataType::LocalVar(self.scm.read_le().unwrap()),
                    0x04 => DataType::Imm(self.scm.read_le::<i8>().unwrap().into()),
                    0x05 => DataType::Imm(self.scm.read_le::<i16>().unwrap().into()),
                    0x06 => DataType::ImmF(self.scm.read_le::<u16>().unwrap() as f32 / 16.0),
                    _ => {
                        self.scm.seek_relative(-1).unwrap();
                        DataType::String(
                            CStr::from_bytes_until_nul(&self.scm.read_le::<[u8; 8]>().unwrap())
                                .unwrap()
                                .to_str()
                                .unwrap()
                                .to_string(),
                        )
                    }
                }
            };

            if matches!(ty, DataType::Eoal) {
                break;
            }
            res.push(ty);
        }
        res
    }

    fn handle_neg_obj_id(&self, id: i32, name: String) -> (u32, String) {
        if id < 0 {
            return (0, self.obj_names[-id as usize].clone());
        }

        (id as u32, name)
    }
}

fn run_scriptengine(
    mut script_engine: ResMut<ScriptEngine>,
    commands: Commands,
    handles: ResMut<ObjHandles>,
) {
    script_engine.step(commands, handles);
}

pub struct ScriptEnginePlugin;

impl Plugin for ScriptEnginePlugin {
    fn build(&self, app: &mut App) {
        app.insert_resource(ScriptEngine::new(
            std::fs::read(GTA_DIR.join("data/main.scm")).unwrap(),
        ))
        .add_systems(FixedUpdate, run_scriptengine);
    }
}

